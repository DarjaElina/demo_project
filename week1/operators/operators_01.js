let a = 10;
let b = 5;

// + operator does addition
let sum = a + b;

// - operator does subtraction
let difference = a - b;

// * operator does multiplication
let product = a * b;

// / operator does division
let quotient = a / b;

// % operator gets the reminder (modulus)
let remainder = a % b;

console.log('The sum of a and b is:', sum);
console.log('The difference between a and b is:', difference);
console.log('The product of a and b is:', product);
console.log('The quotient of and b is:', quotient);
console.log('The remainder of a and b is:', remainder);